a = 3

# Add 4 to a
a += 4  # Equivalent to a = a + 4
print(a)  # Output: 7

# Subtract 3 from a
a -= 3  # Equivalent to a = a - 3
print(a)  # Output: 4

# Multiply a by 2
a *= 2  # Equivalent to a = a * 2
print(a)  # Output: 8

# Divide a by 2 (floating-point division)
a /= 2  # Equivalent to a = a / 2
print(a)  # Output: 4.0

# Floor divide a by 2 (integer division)
a //= 2  # Equivalent to a = a // 2
print(a)  # Output: 2.0

# Raise a to the power of 2
a **= 2  # Equivalent to a = a ** 2
print(a)  # Output: 4.0

# Modulo operation: remainder when a is divided by 4
a %= 4  # Equivalent to a = a % 4
print(a)  # Output: 0.0
