# Using range() function to loop from 0 to 9
print("Using range():")
for x in range(10):
    print(x, end=' ')
print()  # Print a newline for separation

# Iterating through a list
print("Using list:")
for x in [1, 2, 3, 4, 5]:
    print(x, end=' ')
print()  # Print a newline for separation

# Iterating through a string
print("Using string:")
for x in "string":
    print(x, end=' ')
else:
    print("\nEnd of loop")  # Print a newline before indicating the end of the loop
